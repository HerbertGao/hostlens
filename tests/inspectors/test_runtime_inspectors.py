# --------------------------------------------------------------------------- #
# Suite-level false-negative sweep (task 3.3)
# --------------------------------------------------------------------------- #


@pytest.mark.parametrize(
    ("domain", "inspector", "fixture", "parameters"),
    [
        ("jvm", "heap", "heap_unreachable.json", _JVM_PARAMS),
        ("jvm", "gc", "gc_unreachable.json", _JVM_PARAMS),
        ("jvm", "threads", "threads_unreachable.json", _JVM_PARAMS),
        ("go", "goroutines", "goroutines_unreachable.json", None),
        ("go", "heap", "heap_unreachable.json", None),
    ],
)
async def test_runtime_absent_target_never_silent_ok(
    domain: str,
    inspector: str,
    fixture: str,
    parameters: dict[str, object] | None,
) -> None:
    # spec runtime-inspector-suite core false-negative guard, swept across all
    # 5: an absent pid (JVM) / unreachable pprof endpoint (Go) MUST be
    # status=exception, never status=ok with a fabricated healthy count.
    _, result = await _run(domain, inspector, fixture, parameters)
    assert result.status == "exception", f"{domain}.{inspector} swallowed an absent target as ok"
    assert result.findings == []
