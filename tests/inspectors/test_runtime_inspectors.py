"""Snapshot tests for the runtime (JVM + Go) builtin inspectors.

`add-runtime-inspectors`: `jvm.heap`, `jvm.gc`, `jvm.threads`,
`go.goroutines`, `go.heap`.

These run the **real** `InspectorRunner` against `ReplayTarget` fixtures (see
`tests/inspectors/fixtures/jvm/` + `fixtures/go/`, generated by
`_record_runtime.py`), exercising the full
`preflight → render → collect → parse → findings` path **offline** — zero real
JVM, zero pprof endpoint, zero jstat / jcmd / curl. Non-root.

The JVM inspectors are replayed with the SAME `parameters={"pid": 4242}` the
recorder used so the rendered command (which embeds the pid via the direct
attach path) matches the recorded fixture byte-for-byte. `jvm.gc` is run with
the same frozen clock the recorder used (it declares a sampling_window, so the
runner requires a clock). Every test asserts `replay.misses == []` so any drift
between the rendered command and the recorded fixture fails loud.

Coverage per inspector (D-5 / D-8): a finding-trigger fixture (asserts a finding
fires — proving detection, no no-op), an ok fixture (zero findings), and an
**exception** fixture modelling the target being absent (pid not found / pprof
port unreachable): empty stdout + non-zero exit → `status=exception`. The
exception tests are the spec false-negative guards (runtime-inspector-suite
「unreachable fail-loud 不假阴」): an absent pid / unreachable endpoint MUST
surface as `status=exception`, never a silent `status=ok` with a fabricated
healthy count.

Per design D-7 the collector shell (jstat column-name anchoring D-4, jvm.gc
window differencing D-5, curl fail-loud D-2) is NOT executed under ReplayTarget
— it is locked at the command-string level by the verbatim fixture capture.
`test_*_command_locked` assertions pin those command strings; real behaviour
(cross-JDK column anchoring / window deltas) is verified on the real-machine
Demo Path.
"""

from __future__ import annotations

import json
from datetime import UTC, datetime
from pathlib import Path

import pytest
import structlog

from hostlens.core.config import Settings
from hostlens.inspectors.loader import load_manifest
from hostlens.inspectors.result import InspectorResult
from hostlens.inspectors.runner import InspectorRunner
from hostlens.targets.registry import TargetRegistry
from hostlens.targets.replay import ReplayTarget

_BUILTIN_ROOT = Path(__file__).resolve().parents[2] / "src" / "hostlens" / "inspectors" / "builtin"
_FIXTURE_ROOT = Path(__file__).resolve().parent / "fixtures"

# Must match `_record_runtime.FROZEN_DT` so the sampling_window inspector
# (jvm.gc) renders identically to the recorded fixture.
FROZEN_DT = datetime(2026, 1, 2, 12, 0, 0, tzinfo=UTC)

# Must match `_record_runtime._JVM_PARAMS`: the JVM inspectors are recorded via
# the direct attach path (pid set), so the rendered command embeds this pid.
_JVM_PARAMS = {"pid": 4242}


def _runner() -> InspectorRunner:
    return InspectorRunner(
        TargetRegistry(),
        settings=Settings(),
        logger=structlog.get_logger("test"),
        clock=lambda: FROZEN_DT,
    )


async def _run(
    domain: str,
    inspector: str,
    fixture: str,
    parameters: dict[str, object] | None = None,
) -> tuple[ReplayTarget, InspectorResult]:
    manifest = load_manifest(_BUILTIN_ROOT / domain / f"{inspector}.yaml")
    replay = ReplayTarget("runtime-host", fixture=_FIXTURE_ROOT / domain / fixture)
    result = await _runner().run(manifest, replay, parameters)
    return replay, result


def _fixture_main_cmd(domain: str, fixture: str, needle: str) -> str:
    data = json.loads((_FIXTURE_ROOT / domain / fixture).read_text(encoding="utf-8"))
    return next(c["cmd"] for c in data["commands"] if needle in c["cmd"])


# --------------------------------------------------------------------------- #
# jvm.heap
# --------------------------------------------------------------------------- #


async def test_jvm_heap_critical_detected() -> None:
    replay, result = await _run("jvm", "heap", "heap_critical.json", _JVM_PARAMS)
    assert replay.misses == []
    assert result.status == "ok"
    assert result.output == {"heap_used_pct": 96.0}
    assert len(result.findings) == 1
    assert result.findings[0].severity == "critical"


def test_jvm_heap_command_locked() -> None:
    # D-4 column-name anchoring + D-6.1 non-empty second gate are locked at the
    # command-string level (NOT executed under replay): the recorded command
    # MUST anchor columns by NAME and re-check the derived scalar is non-empty.
    cmd = _fixture_main_cmd("jvm", "heap_critical.json", "jstat -gc")
    assert "pid=4242" in cmd
    assert 'col["EU"]' in cmd
    assert 'col["OU"]' in cmd
    assert '[ -n "$used" ] || exit 1' in cmd


async def test_jvm_heap_ok_no_findings() -> None:
    replay, result = await _run("jvm", "heap", "heap_ok.json", _JVM_PARAMS)
    assert replay.misses == []
    assert result.status == "ok"
    assert result.output == {"heap_used_pct": 50.0}
    assert result.findings == []


async def test_jvm_heap_unreachable_is_exception_not_false_negative() -> None:
    replay, result = await _run("jvm", "heap", "heap_unreachable.json", _JVM_PARAMS)
    assert replay.misses == []
    # pid absent / cross-user attach → fail-loud → exception, NOT a fabricated
    # status=ok with a healthy heap percent.
    assert result.status == "exception"
    assert result.findings == []


